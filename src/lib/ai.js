function getLocalReply(message) {
  const lower = message.toLowerCase();
  
  if (lower.includes("спасибо") || lower.includes("благодар")) {
    return "Пожалуйста! Я здесь, чтобы поддержать вас. Если захотите поговорить ещё — я рядом.";
  }
  if (lower.includes("стресс") || lower.includes("нервнич")) {
    return "Стресс — частая проблема студентов. Попробуйте дыхание по квадрату: вдох 4 секунды, задержка 4, выдох 4, задержка 4. Повторите 5 раз — это снижает уровень кортизола. А ещё в приложении есть тест на уровень учебного стресса.";
  }
  if (lower.includes("сон") || lower.includes("сплю") || lower.includes("бессонниц")) {
    return "Проблемы со сном сильно влияют на самочувствие. Попробуйте за час до сна убрать телефон и сделать что-то спокойное: почитать книгу, принять тёплую ванну. Если это продолжается больше двух недель — стоит обратиться к специалисту.";
  }
  if (lower.includes("тревог") || lower.includes("тревож")) {
    return "Тревога — это сигнал организма о том, что что-то требует внимания. Попробуйте записать всё, что вас беспокоит, на бумагу — это помогает 'выгрузить' мысли из головы. А потом пройдите тест на тревожность в разделе Тесты.";
  }
  if (lower.includes("одино") || lower.includes("одинок") || lower.includes("нет друзей")) {
    return "Чувство одиночества — тяжёлое переживание. Многие студенты проходят через это, особенно в новом коллективе. Может быть, записаться в какой-нибудь кружок или студенческую организацию? Общие интересы помогают найти близких по духу людей.";
  }
  if (lower.includes("экзамен") || lower.includes("сессия") || lower.includes("зачёт")) {
    return "Сессия — горячее время. Главное: разбейте подготовку на маленькие куски по 25 минут с перерывами (метод Pomodoro). И не забывайте есть и спать — мозгу нужно топливо. Вы справитесь!";
  }
  if (lower.includes("отношен") || lower.includes("расставан") || lower.includes("бросил")) {
    return "Отношения могут приносить и радость, и боль. Дайте себе время прожить эти чувства — грустить нормально. Если тяжело, поговорите с близким другом или запишите свои мысли в дневник.";
  }
  
  // Случайный ответ из заготовленных
  return LOCAL_REPLIES[Math.floor(Math.random() * LOCAL_REPLIES.length)];
}

/**
 * Запрос к Anthropic API (если есть ключ) или локальный ответ.
 */
export async function askAI(history, catalog) {
  const lastMessage = history.filter(m => m.role === 'user').pop()?.content || '';

  // Если нет ключа — используем локальные ответы
  if (!KEY) {
    // Имитация задержки как у реального API
    await new Promise(r => setTimeout(r, 800 + Math.random() * 1200));
    return getLocalReply(lastMessage);
  }

  // Если ключ есть — используем Claude API
  try {
    const res = await fetch('https://api.anthropic.com/v1/messages', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'x-api-key': KEY,
        'anthropic-version': '2023-06-01',
        'anthropic-dangerous-direct-browser-access': 'true',
      },
      body: JSON.stringify({
        model: 'claude-3-5-sonnet-20241022',
        max_tokens: 1000,
        system: AI_SYSTEM + `\nДоступные тесты в приложении: ${catalog || 'пока нет'}.`,
        messages: history.slice(-10).map((m) => ({ role: m.role, content: m.content })),
      }),
    });
    const data = await res.json();
    
    if (data.error) {
      console.warn('[ai] API error:', data.error);
      return getLocalReply(lastMessage);
    }
    
    const text = (data.content || []).map((c) => (c.type === 'text' ? c.text : '')).filter(Boolean).join('\n').trim();
    return text || getLocalReply(lastMessage);
  } catch (err) {
    console.warn('[ai] fetch failed, using local reply:', err.message);
    return getLocalReply(lastMessage);
  }
}